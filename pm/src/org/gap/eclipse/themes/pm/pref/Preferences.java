package org.gap.eclipse.themes.pm.pref;

/**
 * The Class Preferences which contains constants preference keys.
 */
public final class Preferences {

	private Preferences() {
	}

	/**
	 * The Constant PRESENTATION_FONT_SIZE which is the font size to use in
	 * presentation mode.
	 */
	public static final String PRESENTATION_FONT_SIZE = "pm.font.size";

	/**
	 * The Constant NORMAL_THEME_ID which keeps the theme before switching to pm
	 * mode persisted.
	 */
	public static final String NORMAL_THEME_ID = "pm.theme.normal";

	/** The Constant PM_THEME_ID which is the theme to use in pm mode. */
	public static final String PM_THEME_ID = "pm.theme.pmmode";

	/** The Constant ALTERNATE_THEME_ID. */
	public static final String ALTERNATE_THEME_ID = "org.eclipse.e4.ui.css.theme.e4_default";

}
